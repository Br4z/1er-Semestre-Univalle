// Función para agregar un valor a un árbol existente.

function agregarValor(arbol, valor) {
    if (arbol == null) {
        return { value: valor, left: null, right: null };
    } else {
        // Vamos reconstruyendo el árbol
        if (valor <= arbol.value) {
            return { value: arbol.value, left: agregarValor(arbol.left, valor), right: arbol.right };
        } else {
            return { value: arbol.value, left: arbol.left, right: agregarValor(arbol.right, valor) };
        }
    }
}
