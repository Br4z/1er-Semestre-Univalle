# Encontrar el numero menor y mayor en una lista numÃ©rica

lista = [5, 4, 3, -2, 1, 6, 10, 3, 6, 6, 6]

# Inicialicemos los valores

maximo = lista[0]
minimo = lista[0]

for i in lista:
    if i > maximo:
        maximo = i
    if i <= minimo:
        minimo = i
