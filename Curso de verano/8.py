# Solicitar un numero al usuario y retornar una lista con los nÃºmeros impares hasta ese numero (incluyendo el numero)

numero = input("Digite un numero entero positivo: ")

repeticion = True

while repeticion:
    try:
        numero = int(numero)
        for indice in range(1, numero + 1, 2):
            if indice < numero:
                print(indice, end = ",")  
            else: # Para que al ultimo dato no le ponga coma
                print(indice)
        repeticion = False
    except:
        numero = input("Ingrese un numero valido: ")
    