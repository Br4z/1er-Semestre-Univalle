# Escriba un programa que solicite a un usuario una cantidad de segundos y haga su conversión a cuántas horas, minutos y segundos 
# corresponden los segundos digitados por el usuario.


try:

    print("Bienvenido al convertidor de segundos a horas y minutos")
    timeToConvert = int(input("Digite la cantidad de segundo que desea convertir: "))

    hours = timeToConvert//3600

    minutes = (timeToConvert % 3600)//60

    seconds = ((timeToConvert % 3600) % 60)

    print(timeToConvert," segundos equivalen a ",hours, "hora(s), ", minutes, "minutos y ",seconds," segundos.")

except:
    print("No ha ingresado un valor valido")