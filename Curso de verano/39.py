# Volvamos a hacer el ejercicio de la media, pero ahora un poco mas interactivo

cantidad = int(input("¿De cuantos valores necesitas hacer la media? : "))
valores = []

for i in range(cantidad):
    i = i + 1  # Para enumerar como normalmente lo hacemos
    valor = float(input(f"Valor numero {i} : "))
    valores.append(valor)
print(valores)

a = 0

for i in valores:
    a = a + i

print("la media es: ", a / len(valores))


