# "Deletrear una palabra", con este fin podemos usar un while o un for

palabra = input("Ingrese la palabra que quiere deletrear: ")
longitudPalabra = len(palabra)
posicion = 0

while posicion != longitudPalabra: # Esta es una forma de abordar el problema con el while
    letra = palabra[posicion]
    posicion += 1 # Esto es igual a posicion = posicion + 1
    print(letra)

print("")

posicion = 0

for posicion in range (len(palabra)):
    letra = palabra[posicion] 
    print(letra)

print("")

for indice in palabra:
    print(indice)

