# Script para averiguar si una palabra es un palíndromo (dice lo mismo al derecho y al revés)

word = input("Ingrese la palabra que desea verificar: ")
wordLetters = list(word) # Con esto convertimos la palabra en una lista, separando los caracteres
wordLetters.reverse() # Aquí invertimos la lista de caracteres
reverseWord = "".join(wordLetters) # Aquí juntamos los caracteres que nos dejaron la lista invertida

if reverseWord == word:
    print(f"La palabra {word} es un palindromo")
else:
    print(f"La palabra {word} no es un palindromo")

