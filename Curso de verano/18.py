# Script para averiguar si una palabra es un palÃ­ndromo (dice lo mismo al derecho y al revÃ©s)

word = input("Ingrese la palabra que desea verificar: ")
wordLetters = list(word) # Con esto convertimos la palabra en una lista, separando los caracteres
wordLetters.reverse() # AquÃ­ invertimos la lista de caracteres
reverseWord = "".join(wordLetters) # AquÃ­ juntamos los caracteres que nos dejaron la lista invertida

if reverseWord == word:
    print(f"La palabra {word} es un palindromo")
else:
    print(f"La palabra {word} no es un palindromo")

