# Script para saber si un numero es primo

numero = int(input("Ingrese un numero: "))

for i in range(2, numero): # Como el range no toma el numero final, entonces termina la ejecuciÃ³n de inmediato
    if numero % i == 0:
        print("El numero no es primo")
        break # Si encontramos al menos un divisor ademas del 1 y el mismo numero, podemos dar por hecho que ese numero no es primo
else:
    print("El numero es primo")