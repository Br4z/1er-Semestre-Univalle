# Creemos un script donde el usuario pueda crear una lista con un numero de determinado de nombres

nombres = []
numeroNombres = int(input("Ingrese el numero de nombres que quiere en la lista : "))

for i in range(numeroNombres): # como empieza a contar desde cero, o recorre la lista [0, 1, 2, 3, 4] entonces debemos sumar 1
    enumeracion = i + 1
    nombreElegido = input(f"Nombre numero {enumeracion}: ")
    nombres.append(nombreElegido)
print(nombres)

