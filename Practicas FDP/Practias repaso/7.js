/*

Propósito: Determinar si un valor está entre el intervalo (0,1], si está, retorna true, si no está, retorna false.
Contrato:  number -> boolean
Prototipo: intervalo(number)
Ejemplos: 
(1) -> true
(0) -> false

*/

function intervalo(n) {
    if (n > 0 && n <= 1) {
        return true;
    } else {
        return false;
    }
}

console.log(intervalo(1));
console.log(intervalo(0));
console.log(intervalo(0.5));
