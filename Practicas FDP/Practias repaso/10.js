/*

Propósito: Implementar la multiplicación de forma recursiva.
Contrato:  number, number -> number
Prototipo: multiplicacion(number, number)
Ejemplos: 
(32, 114) -> 3648
(3, 4)    -> 12

*/

function multiplicacion(a, b) {
    if (b == 1) {
        return a;
    } else {
        return a + multiplicacion(a, b - 1);
    }
}

console.log(multiplicacion(3, 4));
console.log(multiplicacion(102, 17));
console.log(multiplicacion(32, 114));
