/*

Prop√≥sito: Calcular la sumatoria de un intervalo definido desde "i" hasta "f".
Contrato:  number, number -> number
Prototipo: sumatoria(number, number)
Ejemplos: 
(1, 4) -> 10
(2, 4) -> 9

*/

function sumatoria(i, f) { // Lo que hace el m√©todo de Gauss
    if (i == f) {
        return i;
    } else {
        return f + sumatoria(i, f - 1);
    }
}

console.log(sumatoria(1, 4));
console.log(sumatoria(2, 4));
console.log(sumatoria(8, 9));
console.log(sumatoria(0, 100));
