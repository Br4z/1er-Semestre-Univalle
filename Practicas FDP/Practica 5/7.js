/*

Propósito: Hacer una suma recursiva entre dos números ("a" y "b").
Contrato:  number, number -> number
Prototipo: sumaR(number, number)
Ejemplos: 
(3, 3)      -> 6
(1294, 374) -> 1668

*/

function sumaR(a, b) {
    if (b == 0) {
        return a;
    } else {
        return 1 + sumaR(a, b - 1);
    }
}

console.log(sumaR(3, 3));
console.log(sumaR(1294, 374));
console.log(sumaR(1111, 33));
console.log(sumaR(5, 3));
