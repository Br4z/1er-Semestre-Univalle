/*

Propósito: Calcular el factorial del numero ingresado.
Contrato:  number -> number or string
Prototipo: factorial(number)
Ejemplos: 
(0) -> 1
(10) -> 3628800

*/

function factorial(n) {
    if (n < 0) {
        return "Parámetro Invalido";
    } else if (n <= 1) { 
        return 1;
    } else {
        return n * factorial(n - 1);
    }
}

console.log(factorial(0));
console.log(factorial(1));
console.log(factorial(10));
console.log(factorial(12));
console.log(factorial(18));
