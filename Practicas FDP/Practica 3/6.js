/*

Propósito: Determinar si el numero es divisible entre 3.
Contrato:  number -> boolean
Prototipo: divisiblePor3(number)
Ejemplos: 
(304050) -> true
(23)     -> false

*/

function divisiblePor3(n) {
    if (n % 3 == 0) {
        return true;
    } else {
        return false;
    }
}

console.log(divisiblePor3(304050));
console.log(divisiblePor3(23));
console.log(divisiblePor3(455));
console.log(divisiblePor3(565));
console.log(divisiblePor3(666));
