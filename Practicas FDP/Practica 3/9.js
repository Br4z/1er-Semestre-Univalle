function estatura(e) {
    if (e < 130) {
        return "estatura baja";
    } else if (e > 130 && e <= 169) {
        return "estatura media";
    } else if (e > 170) {
        return "estatura alta";
    } else if (e > 0) {
        return "No entra en los parametros";
    }
}

