/*

Propósito: Verificar si el tercer numero es la suma del primero con el segundo.
Contrato:  number, number, number -> boolean
Prototipo: sumaC(number, number, number)
Ejemplos: 
(2, 3, 5)          -> true
(1782, 3999, 8523) -> false

*/

function sumaC(numero1, numero2, numero3) {
    if (numero1 + numero2 == numero3) {
        return true;
    } else {
        return false;
    }
}

console.log(sumaC(2, 3, 5));
console.log(sumaC(1782, 3999, 8523));
console.log(sumaC(1562, -9854, -8292));
