const { cons, first, rest, isEmpty, isList } = require("functional-light");

/*

Propósito: copiar una lista de forma recursiva.
Contrato:  list -> list
Prototipo: copy(list)
Ejemplos: 
([])    -> []
([[9]]) -> [[9]]

*/

function copy(lista) {
    if (isEmpty(rest(lista))) {
        return lista;
    } else {
        return cons(first(lista), copy(rest(lista)));
    }
}

console.log(copy([]));
console.log(copy([1, 2, 3]));
console.log(copy([1, 2, [3, 4], [1]]));
console.log(copy([[9]]));
