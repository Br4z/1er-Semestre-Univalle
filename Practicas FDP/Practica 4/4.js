/*

Propósito: Determinar si el numero dado se encuentra en el intervalo (3,7].
Contrato:  number -> string
Prototipo: intervalo(number)
Ejemplos: 
(4) -> "Verdadero"
(3) -> "Error"

*/

function intervalo(n) {
    if (n >= 3 && n <= 7) {
        return "Verdadero";
    } else {
        return "Error";
    }
}

console.log(intervalo(3));
console.log(intervalo(8));
console.log(intervalo(5));
