/*

Propósito: Determinar si el numero dado se encuentra en el intervalo (3,10), (32,57) o (100, infinito).
Contrato:  number -> string
Prototipo: intervalo(number)
Ejemplos: 
(734543234) -> "valido"
(13)        -> "NO valido"

*/

function intervalo(n) {
    if ((n > 3 && n < 10) || (n > 32 && n < 57) || n > 100) {
        return "valido";
    } else {
        return "NO valido";
    }
}

console.log(intervalo(734543234));
console.log(intervalo(13));
console.log(intervalo(42));
