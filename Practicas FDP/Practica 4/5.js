/*

Propósito: Determinar si el numero dado se encuentra en el intervalo [-1,13).
Contrato:  number -> boolean
Prototipo: intervalo(number)
Ejemplos: 
(-1) -> true
(13) -> false

*/

function intervalo(n) {
    if (n >= -1 && n < 13) {
        return true;
    } else {
        return false;
    }
}

